"""Build a high-quality mixed dataset for comment usefulness classification.

This pipeline generates a large training corpus by combining:
1) strict, high-precision real-world labels from raw feature exports
2) curated hard-case labels from mid-confidence real-world records
3) synthetic examples to fill language/label quotas

Output:
  - labeled_hq_<count>.jsonl
  - splits/train.jsonl
  - splits/val.jsonl
  - splits/test.jsonl
  - metadata.json

The output schema is compatible with train.py:
  - features (16 fields expected by tensor.rs mapping)
  - label (0 valuable, 1 superfluous)

Usage:
  python build_high_quality_dataset.py \
    --count 75000 \
    --superfluous-ratio 0.35 \
    --output-dir data/hq \
    --seed 42
"""

from __future__ import annotations

import argparse
import json
import random
import re
import sys
from collections import Counter, defaultdict
from pathlib import Path
from typing import Any

from clt.generate_synthetic import (
    FILE_EXTENSIONS,
    LANGUAGES,
    SUPERFLUOUS_TEMPLATES,
    VALUABLE_TEMPLATES,
    generate_superfluous_features,
    generate_valuable_features,
    make_record,
)
from clt.utils import BOOL_FEATURES, load_jsonl, to_bool, to_float, to_int, write_jsonl

LABEL_VALUABLE = 0
LABEL_SUPERFLUOUS = 1

FEATURE_DEFAULTS: dict[str, Any] = {
    "token_overlap_jaccard": 0.0,
    "identifier_substring_ratio": 0.0,
    "comment_token_count": 0,
    "is_doc_comment": False,
    "is_before_declaration": False,
    "is_inline": False,
    "nesting_depth": 0,
    "has_why_indicator": False,
    "has_external_ref": False,
    "imperative_verb_noun": False,
    "is_section_label": False,
    "contains_literal_values": False,
    "references_other_files": False,
    "references_specific_functions": False,
    "mirrors_data_structure": False,
    "comment_code_age_ratio": None,
    "adjacent_node_kind": "",
}

STRICT_SUPERFLUOUS_MIN = 0.82
STRICT_VALUABLE_MAX = 0.18
HARD_CASE_LOW = 0.28
HARD_CASE_HIGH = 0.72

SEPARATOR_RE = re.compile(r"^\s*[-=*_#/|{}()[\]\\.,:;+<>~`]+\s*$")
TICKET_RE = re.compile(r"\b(?:PROJ|JIRA|SEC|INC|ENG|BUG)-\d+\b", re.IGNORECASE)
URL_RE = re.compile(r"https?://", re.IGNORECASE)
WHY_WORD_RE = re.compile(
    r"\b(because|so that|therefore|due to|in order to|workaround|legacy|compat)\b",
    re.IGNORECASE,
)

SUPERFLUOUS_HARD_SUFFIXES = [
    "for readability",
    "for clarity",
    "for consistency",
    "to keep style uniform",
]

VALUABLE_HARD_SUFFIXES = [
    "to avoid duplicate side effects",
    "to preserve deterministic ordering",
    "to prevent regression from legacy clients",
    "to keep retries idempotent",
    "to avoid deadlocks under load",
]

COMPONENT_HINTS = [
    "cache",
    "pipeline",
    "auth",
    "database",
    "queue",
    "scheduler",
    "worker",
    "parser",
    "router",
    "indexer",
]

GENERIC_PATH_PARTS = {
    "Volumes",
    "Users",
    "home",
    "tmp",
    "var",
    "opt",
    "mnt",
    "workspace",
    "workspaces",
    "devspace",
    "ExternalSamsung",
}


def clamp(value: float, minimum: float, maximum: float) -> float:
    """Clamp value to [minimum, maximum]."""
    return max(minimum, min(maximum, value))


def canonical_comment_text(text: str) -> str:
    """Normalize comment text for deduplication and filtering."""
    normalized = " ".join((text or "").replace("\n", " ").split()).strip().lower()
    return normalized


def is_low_quality_comment(text: str) -> bool:
    """Return True if comment text is too noisy for high-quality training."""
    normalized = canonical_comment_text(text)
    if not normalized:
        return True
    if SEPARATOR_RE.match(normalized):
        return True
    if normalized in {"todo", "fixme", "hack", "note", "example", "n/a"}:
        return True
    if normalized.startswith("code generated by"):
        return True

    alpha_count = sum(1 for c in normalized if c.isalpha())
    token_count = len(normalized.split())

    if alpha_count < 2 and not URL_RE.search(normalized) and not TICKET_RE.search(normalized):
        return True
    if token_count > 80:
        return True
    return False


def sanitize_features(features: dict[str, Any] | None) -> dict[str, Any]:
    """Normalize feature object to expected schema and value types."""
    source = features or {}
    out = dict(FEATURE_DEFAULTS)

    for key in FEATURE_DEFAULTS:
        if key not in source:
            continue
        raw = source[key]
        if key in BOOL_FEATURES:
            out[key] = to_bool(raw)
        elif key in {"comment_token_count", "nesting_depth"}:
            out[key] = max(0, to_int(raw))
        elif key == "comment_code_age_ratio":
            out[key] = None if raw is None else to_float(raw, 0.0)
        elif key == "adjacent_node_kind":
            out[key] = "" if raw is None else str(raw)
        else:
            out[key] = clamp(to_float(raw, 0.0), 0.0, 10.0)

    out["token_overlap_jaccard"] = clamp(out["token_overlap_jaccard"], 0.0, 1.0)
    out["identifier_substring_ratio"] = clamp(out["identifier_substring_ratio"], 0.0, 1.0)
    if out["comment_code_age_ratio"] is not None:
        out["comment_code_age_ratio"] = clamp(out["comment_code_age_ratio"], 0.0, 5.0)

    return out


def extract_repo_group(file_path: str) -> str:
    """Derive repository-level group key from a path for anti-leakage splitting."""
    normalized = (file_path or "").replace("\\", "/").strip()
    if not normalized:
        return "unknown_repo"

    if "/personal/" in normalized:
        remainder = normalized.split("/personal/", 1)[1]
        if remainder:
            return remainder.split("/", 1)[0]

    if "/github/" in normalized:
        remainder = normalized.split("/github/", 1)[1]
        if remainder:
            segments = remainder.split("/")
            if len(segments) >= 2:
                return f"{segments[0]}/{segments[1]}"
            return segments[0]

    parts = [p for p in normalized.split("/") if p]
    for part in parts:
        if part in GENERIC_PATH_PARTS:
            continue
        if "." in part:
            continue
        return part

    return parts[-2] if len(parts) > 1 else parts[0]


def superfluous_rule_score(features: dict[str, Any], comment_text: str) -> float:
    """Score evidence that a comment is superfluous."""
    overlap = clamp(float(features["token_overlap_jaccard"]), 0.0, 1.0)
    identifier = clamp(float(features["identifier_substring_ratio"]), 0.0, 1.0)
    tokens = int(features["comment_token_count"])
    has_why = bool(features["has_why_indicator"]) or bool(WHY_WORD_RE.search(comment_text or ""))
    has_external = bool(features["has_external_ref"])

    score = 0.0
    score += 1.35 * min(1.0, overlap / 0.70)
    score += 1.10 * min(1.0, identifier / 0.70)
    score += 0.70 if features["imperative_verb_noun"] else 0.0
    score += 0.85 if features["is_section_label"] else 0.0
    score += 0.35 if tokens <= 6 else 0.0
    score += 0.20 if features["is_before_declaration"] else 0.0
    score += 0.15 if features["is_inline"] else 0.0

    score -= 0.95 if has_why else 0.0
    score -= 0.80 if has_external else 0.0
    score -= 0.60 if features["references_specific_functions"] else 0.0
    score -= 0.50 if features["references_other_files"] else 0.0
    score -= 0.30 if tokens >= 12 else 0.0
    return score


def valuable_rule_score(features: dict[str, Any], comment_text: str) -> float:
    """Score evidence that a comment is valuable."""
    overlap = clamp(float(features["token_overlap_jaccard"]), 0.0, 1.0)
    identifier = clamp(float(features["identifier_substring_ratio"]), 0.0, 1.0)
    tokens = int(features["comment_token_count"])
    has_why = bool(features["has_why_indicator"]) or bool(WHY_WORD_RE.search(comment_text or ""))

    score = 0.0
    score += 0.95 if has_why else 0.0
    score += 0.85 if features["has_external_ref"] else 0.0
    score += 0.65 if features["references_specific_functions"] else 0.0
    score += 0.60 if features["references_other_files"] else 0.0
    score += 0.45 if tokens >= 8 else 0.0
    score += 0.30 if features["is_doc_comment"] else 0.0
    score += 0.25 if features["contains_literal_values"] else 0.0
    score += 0.20 if int(features["nesting_depth"]) >= 1 else 0.0
    score += 0.15 if features["comment_code_age_ratio"] not in (None, 0.0) else 0.0

    score -= 0.90 if overlap >= 0.55 else 0.0
    score -= 0.80 if identifier >= 0.55 else 0.0
    score -= 0.60 if features["is_section_label"] else 0.0
    score -= 0.45 if features["imperative_verb_noun"] and tokens <= 6 else 0.0
    return score


def normalize_record(
    record: dict[str, Any],
    label: int,
    label_source: str,
    dataset_tier: str,
    repo_group: str | None = None,
) -> dict[str, Any]:
    """Normalize a record to a stable schema and attach dataset metadata."""
    language = str(record.get("language", "")).lower()
    features = sanitize_features(record.get("features", {}))
    text = (record.get("comment_text") or "").strip()

    out = {
        "file": str(record.get("file", "")),
        "line": max(1, to_int(record.get("line"), 1)),
        "column": max(0, to_int(record.get("column"), 0)),
        "language": language,
        "comment_text": text,
        "comment_kind": record.get("comment_kind", "line"),
        "heuristic_score": to_float(record.get("heuristic_score"), 0.0),
        "heuristic_confidence": to_float(record.get("heuristic_confidence"), 0.0),
        "features": features,
        "label": int(label),
        "label_source": label_source,
        "dataset_tier": dataset_tier,
        "repo_group": repo_group or extract_repo_group(str(record.get("file", ""))),
    }
    return out


def record_signature(record: dict[str, Any]) -> tuple:
    """Compute a deduplication signature with text + key feature buckets."""
    features = record["features"]
    return (
        int(record["label"]),
        record["language"],
        canonical_comment_text(record["comment_text"]),
        round(float(features["token_overlap_jaccard"]), 2),
        round(float(features["identifier_substring_ratio"]), 2),
        int(features["comment_token_count"]),
        int(bool(features["has_why_indicator"])),
        int(bool(features["has_external_ref"])),
        int(bool(features["imperative_verb_noun"])),
        int(bool(features["is_section_label"])),
        int(bool(features["references_other_files"])),
        int(bool(features["references_specific_functions"])),
        int(bool(features["is_doc_comment"])),
    )


def label_real_world_record(record: dict[str, Any]) -> tuple[int, str, str] | None:
    """Return (label, source, tier) for strict/hard real-world labels, or None."""
    language = str(record.get("language", "")).lower()
    if language not in LANGUAGES:
        return None
    text = (record.get("comment_text") or "").strip()
    if is_low_quality_comment(text):
        return None

    features = sanitize_features(record.get("features", {}))
    heuristic_score = to_float(record.get("heuristic_score"), 0.0)
    sup_score = superfluous_rule_score(features, text)
    val_score = valuable_rule_score(features, text)
    margin = sup_score - val_score

    if (
        heuristic_score >= STRICT_SUPERFLUOUS_MIN
        and sup_score >= 1.90
        and margin >= 0.90
    ):
        return LABEL_SUPERFLUOUS, "real_world_strict", "strict"

    if (
        heuristic_score <= STRICT_VALUABLE_MAX
        and val_score >= 1.25
        and margin <= -0.75
    ):
        return LABEL_VALUABLE, "real_world_strict", "strict"

    if HARD_CASE_LOW <= heuristic_score <= HARD_CASE_HIGH:
        if sup_score >= 2.10 and margin >= 1.00:
            return LABEL_SUPERFLUOUS, "real_world_hard_rule", "hard_case"
        if val_score >= 1.70 and margin <= -0.95:
            return LABEL_VALUABLE, "real_world_hard_rule", "hard_case"

    return None


def collect_real_world_candidates(
    raw_dir: Path,
    labeled_path: Path | None,
) -> dict[tuple[str, int], list[dict]]:
    """Collect strict/hard labels from raw records plus gold fixture/manual labels."""
    by_bucket: dict[tuple[str, int], list[dict]] = defaultdict(list)

    raw_files = sorted(raw_dir.glob("*.jsonl"))
    for path in raw_files:
        for record in load_jsonl(path):
            decision = label_real_world_record(record)
            if decision is None:
                continue
            label, source, tier = decision
            normalized = normalize_record(
                record=record,
                label=label,
                label_source=source,
                dataset_tier=tier,
            )
            by_bucket[(normalized["language"], normalized["label"])].append(normalized)

    if labeled_path and labeled_path.exists():
        for record in load_jsonl(labeled_path):
            language = str(record.get("language", "")).lower()
            label = record.get("label")
            source = record.get("label_source")
            if language not in LANGUAGES:
                continue
            if label not in (LABEL_VALUABLE, LABEL_SUPERFLUOUS):
                continue
            if source not in {"fixture_ground_truth", "manual_review"}:
                continue
            if is_low_quality_comment(record.get("comment_text", "")):
                continue

            normalized = normalize_record(
                record=record,
                label=int(label),
                label_source=str(source),
                dataset_tier="gold",
            )
            by_bucket[(normalized["language"], normalized["label"])].append(normalized)

    # Deduplicate each bucket to reduce leakage from generated code comments.
    deduped: dict[tuple[str, int], list[dict]] = {}
    for bucket, records in by_bucket.items():
        seen: set[tuple] = set()
        unique_records: list[dict] = []
        for record in records:
            sig = record_signature(record)
            if sig in seen:
                continue
            seen.add(sig)
            unique_records.append(record)
        deduped[bucket] = unique_records

    return deduped


def compute_bucket_targets(
    count: int,
    superfluous_ratio: float,
) -> dict[tuple[str, int], int]:
    """Compute exact per-language, per-label target counts."""
    n_langs = len(LANGUAGES)
    totals = {lang: count // n_langs for lang in LANGUAGES}
    for lang in LANGUAGES[: count % n_langs]:
        totals[lang] += 1

    targets: dict[tuple[str, int], int] = {}
    remainders: list[tuple[float, str]] = []
    assigned_sup = 0

    for lang in LANGUAGES:
        lang_total = totals[lang]
        raw_sup = lang_total * superfluous_ratio
        sup = int(raw_sup)
        val = lang_total - sup
        targets[(lang, LABEL_SUPERFLUOUS)] = sup
        targets[(lang, LABEL_VALUABLE)] = val
        assigned_sup += sup
        remainders.append((raw_sup - sup, lang))

    wanted_sup = int(round(count * superfluous_ratio))
    delta = wanted_sup - assigned_sup

    if delta > 0:
        remainders.sort(reverse=True)
        for _, lang in remainders[:delta]:
            targets[(lang, LABEL_SUPERFLUOUS)] += 1
            targets[(lang, LABEL_VALUABLE)] -= 1
    elif delta < 0:
        remainders.sort()
        for _, lang in remainders[: abs(delta)]:
            targets[(lang, LABEL_SUPERFLUOUS)] -= 1
            targets[(lang, LABEL_VALUABLE)] += 1

    return targets


def pick_real_candidates(
    pool: list[dict],
    target: int,
    real_fraction: float,
    rng: random.Random,
    seen_signatures: set[tuple],
) -> list[dict]:
    """Pick real-world records for a bucket, preferring gold/strict and reserving hard-cases."""
    if target <= 0 or not pool:
        return []

    requested = min(len(pool), int(round(target * real_fraction)))
    if requested <= 0:
        return []

    gold = [r for r in pool if r.get("dataset_tier") == "gold"]
    strict = [r for r in pool if r.get("dataset_tier") == "strict"]
    hard = [r for r in pool if r.get("dataset_tier") == "hard_case"]
    rng.shuffle(gold)
    rng.shuffle(strict)
    rng.shuffle(hard)

    selected: list[dict] = []
    hard_target = min(len(hard), int(round(requested * 0.25)))

    for subset in (gold, strict, hard):
        for record in subset:
            if len(selected) >= requested:
                break
            if subset is hard and sum(1 for r in selected if r.get("dataset_tier") == "hard_case") >= hard_target:
                continue
            sig = record_signature(record)
            if sig in seen_signatures:
                continue
            seen_signatures.add(sig)
            selected.append(record)
        if len(selected) >= requested:
            break

    if len(selected) < requested:
        leftovers = gold + strict + hard
        rng.shuffle(leftovers)
        for record in leftovers:
            if len(selected) >= requested:
                break
            sig = record_signature(record)
            if sig in seen_signatures:
                continue
            seen_signatures.add(sig)
            selected.append(record)

    return selected


def mutate_comment_text(
    rng: random.Random,
    text: str,
    label: int,
    hard_case: bool,
) -> str:
    """Apply light text augmentation for diversity while preserving class meaning."""
    out = (text or "").strip()
    if not out:
        out = "comment"

    if hard_case and label == LABEL_SUPERFLUOUS and rng.random() < 0.45:
        out = f"{out} {rng.choice(SUPERFLUOUS_HARD_SUFFIXES)}"
    elif hard_case and label == LABEL_VALUABLE and rng.random() < 0.55:
        out = f"{out} {rng.choice(VALUABLE_HARD_SUFFIXES)}"

    if label == LABEL_VALUABLE and rng.random() < 0.20:
        out = f"{out} ({rng.choice(COMPONENT_HINTS)} path)"

    if label == LABEL_VALUABLE and "TODO" in out and rng.random() < 0.80:
        out = TICKET_RE.sub(f"PROJ-{rng.randint(100, 9999)}", out)

    return out.strip()


def make_hard_case_features(
    rng: random.Random,
    label: int,
    base_features: dict[str, Any],
) -> dict[str, Any]:
    """Move synthetic features toward boundary for difficult examples."""
    features = dict(base_features)

    if label == LABEL_SUPERFLUOUS:
        features["token_overlap_jaccard"] = clamp(
            to_float(features["token_overlap_jaccard"]) * rng.uniform(0.55, 0.88),
            0.20,
            0.58,
        )
        features["identifier_substring_ratio"] = clamp(
            to_float(features["identifier_substring_ratio"]) * rng.uniform(0.55, 0.90),
            0.18,
            0.60,
        )
        features["comment_token_count"] = int(clamp(
            to_int(features["comment_token_count"]) + rng.randint(1, 5),
            4,
            12,
        ))
        if rng.random() < 0.22:
            features["has_why_indicator"] = True
        if rng.random() < 0.14:
            features["has_external_ref"] = True
    else:
        features["token_overlap_jaccard"] = clamp(
            to_float(features["token_overlap_jaccard"]) + rng.uniform(0.05, 0.25),
            0.06,
            0.48,
        )
        features["identifier_substring_ratio"] = clamp(
            to_float(features["identifier_substring_ratio"]) + rng.uniform(0.04, 0.22),
            0.05,
            0.50,
        )
        features["comment_token_count"] = int(clamp(
            to_int(features["comment_token_count"]) - rng.randint(0, 4),
            4,
            14,
        ))
        features["has_why_indicator"] = True if rng.random() < 0.42 else features["has_why_indicator"]
        features["has_external_ref"] = True if rng.random() < 0.26 else features["has_external_ref"]
        if rng.random() < 0.18:
            features["imperative_verb_noun"] = True

    if rng.random() < 0.35:
        features["comment_code_age_ratio"] = round(rng.uniform(0.4, 1.8), 2)

    return sanitize_features(features)


def generate_synthetic_bucket(
    language: str,
    label: int,
    count: int,
    rng: random.Random,
    seen_signatures: set[tuple],
    start_index: int,
    hard_case_fraction: float = 0.30,
) -> list[dict]:
    """Generate synthetic records for a specific language/label bucket."""
    if count <= 0:
        return []

    records: list[dict] = []
    attempts = 0
    max_attempts = max(3000, count * 80)

    sup_categories = list(SUPERFLUOUS_TEMPLATES.keys())
    val_categories = list(VALUABLE_TEMPLATES.keys())

    while len(records) < count and attempts < max_attempts:
        attempts += 1
        hard_case = rng.random() < hard_case_fraction

        if label == LABEL_SUPERFLUOUS:
            category = rng.choice(sup_categories)
            text = rng.choice(SUPERFLUOUS_TEMPLATES[category])
            features = generate_superfluous_features(rng, category)
        else:
            category = rng.choice(val_categories)
            text = rng.choice(VALUABLE_TEMPLATES[category])
            features = generate_valuable_features(rng, category)

        if hard_case:
            features = make_hard_case_features(rng, label, features)

        text = mutate_comment_text(rng, text, label, hard_case)
        if is_low_quality_comment(text):
            continue

        source = "synthetic_hard" if hard_case else "synthetic_strong"
        record = make_record(
            rng=rng,
            label=label,
            comment_text=text,
            features=features,
            language=language,
            label_source=source,
        )

        idx = start_index + len(records)
        group_id = f"synthetic_{language}_{idx // 50:05d}"
        record["file"] = (
            f"/synthetic/{language}/group_{idx // 50:05d}/"
            f"sample_{idx:06d}{FILE_EXTENSIONS[language]}"
        )
        record["repo_group"] = group_id
        record["dataset_tier"] = "hard_case" if hard_case else "synthetic"
        record["features"] = sanitize_features(record["features"])

        sig = record_signature(record)
        if sig in seen_signatures:
            continue
        seen_signatures.add(sig)
        records.append(record)

    if len(records) < count:
        raise RuntimeError(
            f"Could not generate enough synthetic samples for {language}/label={label}: "
            f"wanted {count}, got {len(records)}"
        )

    return records


def build_dataset(
    count: int,
    superfluous_ratio: float,
    seed: int,
    real_fraction: float,
    raw_dir: Path,
    labeled_path: Path | None,
) -> tuple[list[dict], dict]:
    """Construct mixed dataset to requested size and ratio targets."""
    rng = random.Random(seed)
    real_by_bucket = collect_real_world_candidates(raw_dir, labeled_path)
    bucket_targets = compute_bucket_targets(count=count, superfluous_ratio=superfluous_ratio)

    all_records: list[dict] = []
    seen_signatures: set[tuple] = set()
    synthetic_start_index = 0
    bucket_report: dict[str, dict[str, int]] = {}

    for language in LANGUAGES:
        for label in (LABEL_SUPERFLUOUS, LABEL_VALUABLE):
            key = (language, label)
            target = bucket_targets[key]
            pool = list(real_by_bucket.get(key, []))
            rng.shuffle(pool)

            picked_real = pick_real_candidates(
                pool=pool,
                target=target,
                real_fraction=real_fraction,
                rng=rng,
                seen_signatures=seen_signatures,
            )
            needed = target - len(picked_real)

            synthetic = generate_synthetic_bucket(
                language=language,
                label=label,
                count=needed,
                rng=rng,
                seen_signatures=seen_signatures,
                start_index=synthetic_start_index,
                hard_case_fraction=0.30,
            )
            synthetic_start_index += len(synthetic)

            bucket_records = picked_real + synthetic
            all_records.extend(bucket_records)
            bucket_report[f"{language}:{label}"] = {
                "target": target,
                "real_pool": len(pool),
                "real_selected": len(picked_real),
                "synthetic_selected": len(synthetic),
            }

    if len(all_records) != count:
        raise RuntimeError(f"Dataset size mismatch: expected {count}, got {len(all_records)}")

    rng.shuffle(all_records)
    metadata = summarize_records(all_records)
    metadata["bucket_report"] = bucket_report
    metadata["requested_count"] = count
    metadata["requested_superfluous_ratio"] = superfluous_ratio
    metadata["requested_real_fraction"] = real_fraction
    return all_records, metadata


def summarize_records(records: list[dict]) -> dict[str, Any]:
    """Compute aggregate statistics for the generated corpus."""
    labels = Counter()
    languages = Counter()
    sources = Counter()
    tiers = Counter()
    by_lang_label = Counter()
    real_vs_synth = Counter()

    for record in records:
        label = int(record["label"])
        language = record["language"]
        source = record.get("label_source", "unknown")
        tier = record.get("dataset_tier", "unknown")

        labels[label] += 1
        languages[language] += 1
        sources[source] += 1
        tiers[tier] += 1
        by_lang_label[f"{language}:{label}"] += 1
        if str(source).startswith("synthetic"):
            real_vs_synth["synthetic"] += 1
        else:
            real_vs_synth["real_world"] += 1

    total = len(records)
    sup = labels[LABEL_SUPERFLUOUS]
    return {
        "total": total,
        "superfluous": sup,
        "valuable": labels[LABEL_VALUABLE],
        "superfluous_ratio": (sup / total) if total else 0.0,
        "label_distribution": dict(labels),
        "language_distribution": dict(languages),
        "source_distribution": dict(sources),
        "tier_distribution": dict(tiers),
        "language_label_distribution": dict(by_lang_label),
        "real_vs_synthetic": dict(real_vs_synth),
    }


def split_grouped(
    records: list[dict],
    train_ratio: float = 0.70,
    val_ratio: float = 0.15,
    seed: int = 42,
) -> tuple[list[dict], list[dict], list[dict]]:
    """Group-aware split that keeps repo groups in only one split."""
    rng = random.Random(seed)
    groups: dict[str, list[dict]] = defaultdict(list)
    for record in records:
        group = record.get("repo_group") or extract_repo_group(record.get("file", ""))
        groups[str(group)].append(record)

    total = len(records)
    target_total = {
        "train": int(round(total * train_ratio)),
        "val": int(round(total * val_ratio)),
    }
    target_total["test"] = total - target_total["train"] - target_total["val"]

    keys = [(lang, label) for lang in LANGUAGES for label in (LABEL_VALUABLE, LABEL_SUPERFLUOUS)]
    global_counts = Counter((r["language"], int(r["label"])) for r in records)
    target_counts: dict[str, Counter] = {"train": Counter(), "val": Counter(), "test": Counter()}
    for split, ratio in (("train", train_ratio), ("val", val_ratio), ("test", 1.0 - train_ratio - val_ratio)):
        for key in keys:
            target_counts[split][key] = int(round(global_counts[key] * ratio))

    split_records = {"train": [], "val": [], "test": []}
    split_counts = {"train": Counter(), "val": Counter(), "test": Counter()}
    split_sizes = {"train": 0, "val": 0, "test": 0}

    group_items = list(groups.items())
    rng.shuffle(group_items)
    group_items.sort(key=lambda item: len(item[1]), reverse=True)

    for group_id, group_records in group_items:
        group_counter = Counter((r["language"], int(r["label"])) for r in group_records)
        group_size = len(group_records)

        best_split: str | None = None
        best_score: tuple[int, int, int, float] | None = None
        for split in ("train", "val", "test"):
            remaining = target_total[split] - split_sizes[split]
            overflow = max(0, group_size - max(0, remaining))
            fits = 1 if overflow == 0 else 0

            # Prefer splits where this group's label/language mix fills remaining deficits.
            deficit_gain = 0
            for key in keys:
                deficit = max(0, target_counts[split][key] - split_counts[split][key])
                deficit_gain += min(deficit, group_counter[key])

            score = (
                fits,
                -overflow,
                deficit_gain,
                float(remaining),
            )
            if best_score is None or score > best_score:
                best_score = score
                best_split = split

        assert best_split is not None
        split_records[best_split].extend(group_records)
        split_sizes[best_split] += group_size
        for key, cnt in group_counter.items():
            split_counts[best_split][key] += cnt

    for split in ("train", "val", "test"):
        rng.shuffle(split_records[split])

    return split_records["train"], split_records["val"], split_records["test"]


def verify_split_integrity(train: list[dict], val: list[dict], test: list[dict]) -> dict[str, Any]:
    """Verify group leakage and summarize split distributions."""
    sets = {
        "train": {r["repo_group"] for r in train},
        "val": {r["repo_group"] for r in val},
        "test": {r["repo_group"] for r in test},
    }
    leakage = {
        "train_val_group_overlap": len(sets["train"] & sets["val"]),
        "train_test_group_overlap": len(sets["train"] & sets["test"]),
        "val_test_group_overlap": len(sets["val"] & sets["test"]),
    }

    def dist(records: list[dict]) -> dict[str, Any]:
        labels = Counter(int(r["label"]) for r in records)
        langs = Counter(r["language"] for r in records)
        return {
            "total": len(records),
            "label_distribution": dict(labels),
            "language_distribution": dict(langs),
        }

    return {
        "group_leakage": leakage,
        "train": dist(train),
        "val": dist(val),
        "test": dist(test),
    }


def parse_args() -> argparse.Namespace:
    """Parse command-line arguments."""
    parser = argparse.ArgumentParser(description="Build high-quality mixed training dataset")
    parser.add_argument(
        "--raw-dir",
        default="data/raw",
        help="Directory containing raw JSONL exports (default: data/raw)",
    )
    parser.add_argument(
        "--labeled-input",
        default="data/labeled.jsonl",
        help="Optional labeled JSONL to source gold/manual labels (default: data/labeled.jsonl)",
    )
    parser.add_argument(
        "--output-dir",
        default="data/hq",
        help="Output directory for dataset and splits (default: data/hq)",
    )
    parser.add_argument(
        "--count",
        type=int,
        default=75000,
        help="Total output records (default: 75000)",
    )
    parser.add_argument(
        "--superfluous-ratio",
        type=float,
        default=0.35,
        help="Target fraction of superfluous comments (default: 0.35)",
    )
    parser.add_argument(
        "--real-fraction",
        type=float,
        default=0.35,
        help="Per-bucket target fraction sourced from real-world records (default: 0.35)",
    )
    parser.add_argument(
        "--train-ratio",
        type=float,
        default=0.70,
        help="Train split ratio (default: 0.70)",
    )
    parser.add_argument(
        "--val-ratio",
        type=float,
        default=0.15,
        help="Validation split ratio (default: 0.15)",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=42,
        help="Random seed (default: 42)",
    )
    return parser.parse_args()


def main() -> None:
    """Build dataset, write outputs, and print concise report."""
    args = parse_args()

    if args.count <= 0:
        raise ValueError("--count must be > 0")
    if not (0.0 < args.superfluous_ratio < 1.0):
        raise ValueError("--superfluous-ratio must be between 0 and 1")
    if not (0.0 <= args.real_fraction <= 1.0):
        raise ValueError("--real-fraction must be between 0 and 1")
    if args.train_ratio + args.val_ratio >= 1.0:
        raise ValueError("train_ratio + val_ratio must be < 1.0")

    raw_dir = Path(args.raw_dir)
    labeled_input = Path(args.labeled_input) if args.labeled_input else None
    output_dir = Path(args.output_dir)
    split_dir = output_dir / "splits"
    output_dir.mkdir(parents=True, exist_ok=True)
    split_dir.mkdir(parents=True, exist_ok=True)

    records, metadata = build_dataset(
        count=args.count,
        superfluous_ratio=args.superfluous_ratio,
        seed=args.seed,
        real_fraction=args.real_fraction,
        raw_dir=raw_dir,
        labeled_path=labeled_input,
    )

    train, val, test = split_grouped(
        records=records,
        train_ratio=args.train_ratio,
        val_ratio=args.val_ratio,
        seed=args.seed,
    )
    split_info = verify_split_integrity(train, val, test)
    metadata["split_summary"] = split_info

    dataset_path = output_dir / f"labeled_hq_{args.count}.jsonl"
    write_jsonl(records, dataset_path)
    write_jsonl(train, split_dir / "train.jsonl")
    write_jsonl(val, split_dir / "val.jsonl")
    write_jsonl(test, split_dir / "test.jsonl")

    with open(output_dir / "metadata.json", "w") as f:
        json.dump(metadata, f, indent=2)

    print(f"Built dataset: {dataset_path}", file=sys.stderr)
    print(f"  Total:        {metadata['total']}", file=sys.stderr)
    print(f"  Superfluous:  {metadata['superfluous']} ({metadata['superfluous_ratio']:.3f})", file=sys.stderr)
    print(f"  Valuable:     {metadata['valuable']}", file=sys.stderr)
    print("  Language distribution:", file=sys.stderr)
    for lang in LANGUAGES:
        print(f"    {lang}: {metadata['language_distribution'].get(lang, 0)}", file=sys.stderr)
    print("  Real vs synthetic:", file=sys.stderr)
    for source, count in sorted(metadata["real_vs_synthetic"].items()):
        print(f"    {source}: {count}", file=sys.stderr)

    leakage = metadata["split_summary"]["group_leakage"]
    print("  Group leakage checks:", file=sys.stderr)
    print(
        "    train/val={train_val_group_overlap}, train/test={train_test_group_overlap}, "
        "val/test={val_test_group_overlap}".format(**leakage),
        file=sys.stderr,
    )


if __name__ == "__main__":
    main()
