"""Tests for build_high_quality_dataset.py."""

import pytest

from clt.build_high_quality_dataset import (
    LANGUAGES,
    compute_bucket_targets,
    extract_repo_group,
    is_low_quality_comment,
    split_grouped,
)


def _make_record(group: str, language: str, label: int, idx: int) -> dict:
    """Create a compact record for split tests."""
    return {
        "file": f"/repo/{group}/src/file_{idx}.rs",
        "line": idx + 1,
        "column": 0,
        "language": language,
        "comment_text": f"comment {idx}",
        "comment_kind": "line",
        "heuristic_score": 0.0,
        "heuristic_confidence": 0.0,
        "features": {
            "token_overlap_jaccard": 0.1,
            "identifier_substring_ratio": 0.1,
            "comment_token_count": 5,
            "is_doc_comment": False,
            "is_before_declaration": True,
            "is_inline": False,
            "nesting_depth": 0,
            "has_why_indicator": False,
            "has_external_ref": False,
            "imperative_verb_noun": False,
            "is_section_label": False,
            "contains_literal_values": False,
            "references_other_files": False,
            "references_specific_functions": False,
            "mirrors_data_structure": False,
            "comment_code_age_ratio": None,
            "adjacent_node_kind": "function_item",
        },
        "label": label,
        "label_source": "test",
        "dataset_tier": "test",
        "repo_group": group,
    }


def test_compute_bucket_targets_sums_and_ratio():
    """Targets should sum to count and approximate requested ratio."""
    count = 750
    ratio = 0.35
    targets = compute_bucket_targets(count=count, superfluous_ratio=ratio)

    total = sum(targets.values())
    assert total == count

    superfluous = sum(v for (lang, label), v in targets.items() if label == 1)
    expected = round(count * ratio)
    assert superfluous == expected

    for language in LANGUAGES:
        assert (language, 0) in targets
        assert (language, 1) in targets
        assert targets[(language, 0)] > 0
        assert targets[(language, 1)] > 0


def test_split_grouped_no_group_leakage():
    """A repo group should appear in only one split."""
    records = []
    idx = 0

    # Build records across multiple groups/languages/labels.
    for group_num in range(30):
        group = f"repo_{group_num:02d}"
        for _ in range(8):
            language = LANGUAGES[group_num % len(LANGUAGES)]
            label = 1 if (group_num % 3 == 0) else 0
            records.append(_make_record(group, language, label, idx))
            idx += 1

    train, val, test = split_grouped(records, train_ratio=0.7, val_ratio=0.15, seed=42)

    train_groups = {r["repo_group"] for r in train}
    val_groups = {r["repo_group"] for r in val}
    test_groups = {r["repo_group"] for r in test}

    assert train_groups.isdisjoint(val_groups)
    assert train_groups.isdisjoint(test_groups)
    assert val_groups.isdisjoint(test_groups)

    assert len(train) + len(val) + len(test) == len(records)


